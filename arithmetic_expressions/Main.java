package arithmetic_expressions;

public class Main {

	public static void main(String[] args) {
		int apples = 10;
		apples = apples + 1;
		apples = apples * 2;
		apples = apples - 1;
		apples = apples / 7;
		apples = apples % 3;   //this gives the remainder of division
		
		System.out.println(apples);
		

	}

}
